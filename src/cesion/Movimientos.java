      public void llenar() {
        cargarDriver();
        Conexion cn = new Conexion();
        String dbURL = "jdbc:mysql://" + cn.ip + ":3306/" + cn.base;
        String username = cn.usuario;
        String password = cn.pass;
        Connection dbCon = null;
        Statement stmt = null;
        ResultSet rs = null;
        String Buscar=this.jLabel1.getText();
        String query = "select id,fecha,tipo,codigo,cantidad from movimientos WHERE codigo='"+Buscar+"' ";
        try {
            //getting database connection to MySQL server 
            dbCon = DriverManager.getConnection(dbURL, username, password);
            //getting PreparedStatment to execute query 
            stmt = dbCon.prepareStatement(query);
            //Resultset returned by query 
            rs = stmt.executeQuery(query);
            int son=0;
            
            while (rs.next()) {

                Object[] object = new Object[5];
                object[0] = rs.getString(2);
                object[1] = rs.getString(3);
                object[2] = rs.getString(4);
                object[3] = rs.getString(5);
                int cant=rs.getInt(5);
                if (rs.getString(3).equals("I")){
                    son=son+cant;
                }
                 if (rs.getString(3).equals("FC")){
                    son=son+cant;
                }
                 if (rs.getString(3).equals("TI")){
                    son=son-cant;
                }
                object[4] = String.valueOf(son);

                modelo.addRow(object);
            }

        } catch (SQLException ex) {
            System.out.println(ex.getMessage().toString());
        }

    }
 
    
    
